-- ============================================================================
-- complete_migration_script_2.sql
-- COMPLETE from-scratch install for the RMU Claims Approval System.
--
-- Builds the entire current schema (all tables) plus seed data for the
-- reference/config tables (roles, settings, workflow, ranks & rates, faculty,
-- department, programme, course, bank branches, holidays). Transactional
-- tables (users, claims, logs, drafts, etc.) are created empty.
--
-- Reflects every change through 2026-07: HR role + hr_employees, multi-class
-- (VARCHAR widths incl. combined classes), previous-month claims, dd/mm/yyyy,
-- and the separate archive database.
--
-- Apply with:  mysql -u <user> -p < complete_migration_script_2.sql
-- (Rename `doc-app` below if your database uses a different name.)
-- ============================================================================

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;
SET SQL_MODE = 'NO_AUTO_VALUE_ON_ZERO';

CREATE DATABASE IF NOT EXISTS `doc-app` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE `doc-app`;

-- ---------------------------------------------------------------------------
-- Table: academic_year
-- ---------------------------------------------------------------------------
DROP TABLE IF EXISTS `academic_year`;
CREATE TABLE `academic_year` (
  `id` int NOT NULL AUTO_INCREMENT,
  `start_month` varchar(15) COLLATE utf8mb4_unicode_ci NOT NULL,
  `end_month` varchar(15) COLLATE utf8mb4_unicode_ci NOT NULL,
  `start_year` year NOT NULL,
  `end_year` year NOT NULL,
  `archived` tinyint(1) DEFAULT '0',
  `name` varchar(15) COLLATE utf8mb4_unicode_ci GENERATED ALWAYS AS (concat(`start_year`,_utf8mb4'-',`end_year`)) VIRTUAL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ---------------------------------------------------------------------------
-- Table: admin_logs
-- ---------------------------------------------------------------------------
DROP TABLE IF EXISTS `admin_logs`;
CREATE TABLE `admin_logs` (
  `log_id` int NOT NULL AUTO_INCREMENT,
  `log_date` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`log_id`)
