-- ---------------------------------------------------------------------------
-- Table: audit_log
-- ---------------------------------------------------------------------------
DROP TABLE IF EXISTS `audit_log`;
CREATE TABLE `audit_log` (
  `audit_id` bigint unsigned NOT NULL AUTO_INCREMENT,
  `actor_id` int DEFAULT NULL COMMENT 'userId of the person acting; NULL = system / cron',
  `actor_role` varchar(20) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `action` varchar(80) COLLATE utf8mb4_unicode_ci NOT NULL COMMENT 'Verb constant: CLAIM_SUBMITTED, CLAIM_APPROVED, ACCOUNT_ACTIVATED …',
  `entity_type` varchar(40) COLLATE utf8mb4_unicode_ci DEFAULT NULL COMMENT 'Domain noun: claim, user, setting, payment …',
  `entity_id` int DEFAULT NULL COMMENT 'Primary key of the affected row',
  `detail` json DEFAULT NULL COMMENT 'Structured before/after snapshot or context',
  `ip_address` varchar(45) COLLATE utf8mb4_unicode_ci DEFAULT NULL,
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`audit_id`),
  KEY `idx_al_actor` (`actor_id`),
  KEY `idx_al_action` (`action`),
  KEY `idx_al_entity` (`entity_type`,`entity_id`),
  KEY `idx_al_created` (`created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Append-only audit trail. Do NOT DELETE rows.';

